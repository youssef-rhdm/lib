#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*head;
	char	*str;
	size_t	total_size;

	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	total_size = ft_strlen(s1) + ft_strlen(s2);
	str = malloc(total_size + 1);
	if (str == NULL)
		return (NULL);
	head = str;
	while (*s1)
		*str++ = *s1++;
	while (*s2)
		*str++ = *s2++;
	*str = '\0';
	return (head);
}
